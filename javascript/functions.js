function createRect(x, y, width, height, type, color, font, fontSize, text, img, invert) {
  switch (type) {
    case "rect":
      canvasContext.fillStyle = color;
      canvasContext.fillRect(x, y, width, height);
      break;

    case "text":
      canvasContext.font = `${fontSize}px ${font}`;
      canvasContext.fillStyle = color;
      canvasContext.fillText(text, x, y);
      break;

    case "image":
      if (!invert) {
        canvasContext.save();
        canvasContext.drawImage(img, x, y, width, height);
        canvasContext.restore();
      } else {
        canvasContext.save();
        canvasContext.translate(x + width, y);
        canvasContext.scale(-1, 1);
        canvasContext.drawImage(img, 0, 0, width, height);
        canvasContext.restore();
      }
      break;

    default:
      break;
  }
}

